import ContentPage from "@/components/ContentPage";
import { ArrowFlow } from "@/components/WireframeBlock";

export default function ProgressiveDisclosure() {
  return (
    <ContentPage title="Прогрессивное раскрытие" subtitle="Сначала сигнал, потом контекст, потом детали.">
      <p>
        Progressive disclosure — это принцип, при котором интерфейс показывает информацию слоями. 
        Пользователь не получает всё сразу. Он сначала видит главное, 
        а затем сам решает, насколько глубоко погружаться.
      </p>

      <ArrowFlow items={["Сигнал", "Контекст", "Детали", "Действие"]} />

      <h2>Как это работает</h2>
      <ul>
        <li><strong>Первый слой — сигнал.</strong> Что произошло? Что требует внимания? Строка в списке, бейдж, число.</li>
        <li><strong>Второй слой — контекст.</strong> Почему это важно? Summary, мета-информация, краткая аналитика.</li>
        <li><strong>Третий слой — детали.</strong> Полная информация об объекте: история, связи, вложенные данные.</li>
        <li><strong>Четвёртый слой — действие.</strong> Что с этим делать? Кнопки, формы, переходы к следующему шагу.</li>
      </ul>

      <h2>Почему это важно</h2>
      <p>
        В B2B продукте у пользователя десятки или сотни объектов. 
        Если каждый объект сразу показывает все свои данные, экран перегружен и бесполезен. 
        Прогрессивное раскрытие позволяет быстро сканировать список, 
        погружаться только туда, где нужно, и не тратить время на остальное.
      </p>
    </ContentPage>
  );
}
